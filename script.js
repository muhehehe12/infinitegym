// CHATBOT
// ==========================================
let chatOpen = false;
const chatWindow = document.getElementById('chatbot-window');
const chatMessages = document.getElementById('chatMessages');
const chatInput = document.getElementById('chatInput');

function toggleChat() {
  chatOpen = !chatOpen;
  chatWindow.classList.toggle('open', chatOpen);
  if (chatOpen && chatMessages.children.length === 0) {
    setTimeout(() => addBotMessage(translations[currentLang].botIntro), 400);
  }
  if (chatOpen) setTimeout(() => chatInput.focus(), 500);
}

function addUserBubble(text) {
  const el = document.createElement('div');
  el.className = 'chat-bubble bubble-user';
  el.textContent = text;
  chatMessages.appendChild(el);
  chatMessages.scrollTop = chatMessages.scrollHeight;
}

function addBotMessage(text) {
  const typing = document.createElement('div');
  typing.className = 'chat-typing';
  typing.innerHTML = '<span></span><span></span><span></span>';
  chatMessages.appendChild(typing);
  chatMessages.scrollTop = chatMessages.scrollHeight;

  setTimeout(() => {
    typing.remove();
    const el = document.createElement('div');
    el.className = 'chat-bubble bubble-bot';
    el.textContent = text;
    chatMessages.appendChild(el);
    chatMessages.scrollTop = chatMessages.scrollHeight;
  }, 900);
}

function getBotResponse(msg) {
  const lower = msg.toLowerCase();
  const t = translations[currentLang];
  
  if (lower.includes('location') || lower.includes('locatie') || lower.includes('unde')) return t.botLocation;
  if (lower.includes('hour') || lower.includes('orar') || lower.includes('program')) return t.botHours;
  if (lower.includes('trainer') || lower.includes('antrenor') || lower.includes('coach')) return t.botTrainer;
  if (lower.includes('price') || lower.includes('pret') || lower.includes('abonament') || lower.includes('cost')) return t.botPrice;
  
  return t.botGeneric;
}

function sendChat() {
  const msg = chatInput.value.trim();
  if (!msg) return;
  addUserBubble(msg);
  chatInput.value = '';
  const reply = getBotResponse(msg);
  setTimeout(() => addBotMessage(reply), 200);
}

